            </svg></button>Tips for finding potential processes to be automated</h3>
    <ul>
        <li>
            What are most of the people in the organization working on? More people -&gt; more work -&gt; more tasks to automate.
        </li>
        <li>
            Ideas for automation can come from the person completing the process or someone else involved in the process.

        </li>
        <li>
            You might have an idea for a process that would improve efficiency, but can not be completed even manually at the moment. Sometimes automation enables you to start creating completely new processes instead of just automating existing ones.
        </li>
        <li>
            Collect all the possible automation ideas in a backlog. Then decide which process it makes sense to start with: depending on your use case, it could be the easiest process to automate, or the one that you think will have the biggest return of investment.
            Do not just pick the first one that you find: having good first experiences is important, both for people and for organizations!
        </li>
    </ul>
