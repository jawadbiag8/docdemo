            </path>
            </svg></button>Creating Automation Roadmap</h2>
    <p>After going through the all of the processes RPA Business Analyst selects the processes suitable for automation and builds automation roadmap based on prioritization matrix.</p>
    <p>Prioritization matrix helps to prioritize the automation journey and deliver most value with minimal effort in shortest time, hence quickly impacts an organization in realizing the potential of Autosphere. </p>
    <p>Points to be considered in Prioritization Matrix: </p>
    <ul>
        <li>Start automating the “Low hanging fruits” Processes that present a combination of High potential and low Complexity”.</li>
        <li>High Potential is driven by volume and time reduction.</li>
        <li>Complexity is driven by characteristics such as presence of structured data number of rules or steps etc.</li>
    </ul>
