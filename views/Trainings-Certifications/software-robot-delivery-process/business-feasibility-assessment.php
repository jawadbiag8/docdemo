            </path>
            </svg></button>Is the process stable, or does it change all the time?</h2>
    <ul>
        <li>Do you anticipate that the process will stay mostly the same for some time? Substantial changes in the process will require more development work to adapt the robot to new requirements, so if you expect the process to change often,
            it might be reason enough to decide not to automate it.</li>
    </ul>
