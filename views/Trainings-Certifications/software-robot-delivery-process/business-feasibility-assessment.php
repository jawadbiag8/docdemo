            </path>
            </svg></button>Does the process repeat often?</h2>
    <ul>
        <li>
            <p>The more often the process needs to be completed manually, the better the potential benefits of automating it. Faster completion, fewer errors, time to focus on more exciting things!</p>
        </li>
        <li>
            <p>If the process is manual and time-consuming but does not happen often, you need to consider the cost of implementing the automation. Does the effort pay itself back (positive ROI)?</p>
        </li>
    </ul>

