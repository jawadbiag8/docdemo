    <p>Ok, after the technical feasibility assessment, we know that the process could be automated. But just because something is technically doable, it does not necessarily make business sense to do it!</p>
    <p>When evaluating the feasibility of a Robotic Process Automation project, you should include a business feasibility assessment phase, to understand if the automation would lead to a positive return on investment or not.</p>
    <p>Ask yourself these questions:</p>
